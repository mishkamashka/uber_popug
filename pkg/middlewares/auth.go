package middlewares

import (
	"errors"
	"github.com/gin-gonic/gin"
	"time"
	"uber-popug/pkg/types"

	"github.com/dgrijalva/jwt-go"
)

var jwtKey = []byte("supersecretkey")

func Auth() gin.HandlerFunc {
	return func(context *gin.Context) {
		tokenString := context.GetHeader("Authorization")
		if tokenString == "" {
			context.JSON(401, gin.H{"error": "request does not contain an access token"})
			context.Abort()
			return
		}
		err := ValidateToken(context, tokenString)
		if err != nil {
			context.JSON(401, gin.H{"error": err.Error()})
			context.Abort()
			return
		}
		context.Next()
	}
}

func AdminAuth() gin.HandlerFunc {
	return func(context *gin.Context) {
		tokenString := context.GetHeader("Authorization")
		if tokenString == "" {
			context.JSON(401, gin.H{"error": "request does not contain an access token"})
			context.Abort()
			return
		}
		err := ValidateAdminToken(context, tokenString)
		if err != nil {
			context.JSON(401, gin.H{"error": err.Error()})
			context.Abort()
			return
		}
		context.Next()
	}
}

func ValidateToken(ctx *gin.Context, signedToken string) (err error) {
	token, err := jwt.ParseWithClaims(
		signedToken,
		&types.JWTClaim{},
		func(token *jwt.Token) (interface{}, error) {
			return []byte(jwtKey), nil
		},
	)
	if err != nil {
		return
	}
	claims, ok := token.Claims.(*types.JWTClaim)
	if !ok {
		err = errors.New("couldn't parse claims")
		return
	}
	if claims.ExpiresAt < time.Now().Local().Unix() {
		err = errors.New("token expired")
		return
	}

	ctx.Set("userID", claims.UserID)

	return
}

func ValidateAdminToken(ctx *gin.Context, signedToken string) (err error) {
	token, err := jwt.ParseWithClaims(
		signedToken,
		&types.JWTClaim{},
		func(token *jwt.Token) (interface{}, error) {
			return []byte(jwtKey), nil
		},
	)
	if err != nil {
		return
	}
	claims, ok := token.Claims.(*types.JWTClaim)
	if !ok {
		err = errors.New("couldn't parse claims")
		return
	}
	if claims.ExpiresAt < time.Now().Local().Unix() {
		err = errors.New("token expired")
		return
	}
	if claims.Role != "admin" {
		err = errors.New("role must be admin")
		return
	}

	ctx.Set("userID", claims.UserID)

	return
}
